              <h4>ベンチャーに発展したプロジェクト</h4>
              <img src="img/idea.jpg" width="100%">
              <p>iGEMはiGEMへの参加やアワード授賞をゴールとせずに、その後も研究を継続することを強く推奨しています。2004年以来30,000人以上のiGEMerの中から沢山のベンチャー企業が誕生しています。</p>
              <p>ASIMOVは2017年3月に、iGEM2008でbest pesentation賞準優勝を獲得したワシントン大学(シアトル)のチームがベースになって設立された、MIT初のベンチャー企業です。</p>
              <p>
                2008年のiGEMでは、バクテリアがもつプラスミドを用いた、個体間での遺伝子の水平伝播の性質を発展させた技術を提案しました。水平伝播をもつことが知られている大腸菌を用いて、その培養条件を調整することで、真核生物である出芽酵母に目的となるプラスミドの遺伝子を挿入するシステムを構築しました。これらの研究をまとめ、わかりやすく発表したことでiGEM2008ではbest pesentation賞を獲得しました。
